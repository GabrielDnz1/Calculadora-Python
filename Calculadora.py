print("Calculadora")
n1 = int(input("Digite um número:"))
n2 = int(input("Digite outro número:"))
op = str(input("Digite um Operador(+, -, x, /):"))
if op == "+":
    print(n1 + n2)
elif op == "-":
    print(n1 - n2)
elif op == "x":
    print(n1 * n2)
elif op == "/":
    print(n1 / n2)



